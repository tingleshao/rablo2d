class Color
  
  @@black = '#000000'
  @@white = '#FFFFFF'
  @@blue = '#00FFFF'   
  @@red = '#FF0000'
  
  def initialize()
  end
 
  def self.black
    @@black
  end
  
  def self.white
    @@white
  end 
  
  def self.blue
    @@blue
  end
 
  def self.red
    @@red
  end

  def self.default
    @@black
  end
end
